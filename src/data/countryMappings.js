// Normalize a world-atlas country name to match GADM NAME_0
export function normalizeCountryName(atlasName) {
  return ATLAS_TO_GADM[atlasName] || atlasName
}

// Reverse lookup: GADM name → ISO3 (for names that were normalized)
const GADM_TO_ISO3 = Object.fromEntries(
  Object.entries(ATLAS_TO_GADM).map(([atlas, gadm]) => [gadm, COUNTRY_TO_ISO3[atlas]])
)

export function getISO3ForCountry(countryName) {
  return COUNTRY_TO_ISO3[countryName] || GADM_TO_ISO3[countryName] || null
}
